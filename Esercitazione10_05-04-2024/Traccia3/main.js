let variabile1 = prompt("Inserisci la prima variabile");
let variabile2 = prompt("Inserisci la seconda variabile");

let somma = parseInt(variabile1) + parseInt(variabile2);
let sottrazione = variabile1 - variabile2;
let moltiplicazione = variabile1 * variabile2;
let divisione = variabile1 / variabile2;
let potenza = variabile1 ** variabile2;
let modulo = variabile1 % variabile2;

console.log(`Con i numeri da te scelti, i risultati delle varie operazioni sono: somma = ${somma}, sottrazione = ${sottrazione}, moltiplicazione = ${moltiplicazione}, divisione = ${divisione}, potenza = ${potenza}, modulo ${modulo}`);

